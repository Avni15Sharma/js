const month = "march"

switch (month) {
    case "jan":
        console.log("January");
        break;  //if break is not used with cases ,then the code inside matching case and of all cases below it will execute "except default"
    case "feb":
        console.log("feb");
        break;
    case "march":
        console.log("march");
        break;
    case "april":
        console.log("april");
        break;

    default:
        console.log("default case match");
        break;
}